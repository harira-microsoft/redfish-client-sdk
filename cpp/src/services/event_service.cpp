RedfishResponse EventServiceHandle::submit_test_event(const nlohmann::json& event_data) {
    nlohmann::json default_body = {
        {"EventType",      "Alert"},
        {"EventId",        "1"},
        {"EventTimestamp", iso8601_now()},
        {"Severity",       "OK"},
        {"Message",        "Test event generated by Redfish Client SDK."},
        {"MessageId",      "Base.1.8.GeneralError"},
    };

    nlohmann::json body = default_body;
    if (!event_data.is_null()) {
        for (auto& [k, v] : event_data.items())
            body[k] = v;
    }

    std::map<std::string, std::string> headers;
    AuthManager::attach_auth(auth_state_, headers);
    auto action_uri = service_uri() + "/Actions/EventService.SubmitTestEvent";
    auto raw = http_.request("POST", action_uri, headers, body.dump());
    return build_response(raw.status_code, raw.headers, raw.body_text);
}

