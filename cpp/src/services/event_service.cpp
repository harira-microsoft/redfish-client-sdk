static std::string iso8601_now() {
    auto now = std::chrono::system_clock::now();
    auto t   = std::chrono::system_clock::to_time_t(now);
    std::ostringstream ss;
    ss << std::put_time(std::gmtime(&t), "%Y-%m-%dT%H:%M:%SZ");
    return ss.str();
}

EventServiceHandle::EventServiceHandle(
    IHttpClient&                             http,
    const AuthState&                         auth_state,
    const std::map<std::string, std::string>& discovery_map
)
    : http_(http), auth_state_(auth_state), discovery_map_(discovery_map)
{}

std::string EventServiceHandle::service_uri() const {
    auto it = discovery_map_.find("EventService");
    return it != discovery_map_.end() ? it->second : "/redfish/v1/EventService";
}

RedfishResponse EventServiceHandle::get_service_info() {
    std::map<std::string, std::string> headers;
    AuthManager::attach_auth(auth_state_, headers);
    auto raw = http_.request("GET", service_uri(), headers);
    return build_response(raw.status_code, raw.headers, raw.body_text);
}

RedfishResponse EventServiceHandle::subscribe(
    const std::string&              destination,
    const std::vector<std::string>& event_types,
    const std::vector<std::string>& registry_prefixes,
    const std::vector<std::string>& message_ids,
    const std::vector<std::string>& resource_types,
    const std::string&              event_format_type,
    const std::string&              context,
    const std::string&              protocol,
    const std::string&              subscription_type
) {
    nlohmann::json body = {
        {"Destination",      destination},
        {"Protocol",         protocol},
        {"SubscriptionType", subscription_type},
    };
    if (!event_types.empty())       body["EventTypes"]       = event_types;
    if (!registry_prefixes.empty()) body["RegistryPrefixes"] = registry_prefixes;
    if (!message_ids.empty())       body["MessageIds"]       = message_ids;
    if (!resource_types.empty())    body["ResourceTypes"]    = resource_types;
    if (!event_format_type.empty()) body["EventFormatType"]  = event_format_type;
    if (!context.empty())           body["Context"]          = context;

    std::map<std::string, std::string> headers;
    AuthManager::attach_auth(auth_state_, headers);
    auto subs_uri = service_uri() + "/Subscriptions";
    auto raw = http_.request("POST", subs_uri, headers, body.dump());
    return build_response(raw.status_code, raw.headers, raw.body_text);
}

